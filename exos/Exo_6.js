let color = "Alexou";

function switchColor(color) {
    if (color === "rouge") {
        console.log("C'est rouge");
    } else if (color === "bleu") {
        console.log("C'est bleu");
    } else if (color === "vert") {
        console.log("C'est vert");
    } else if (color === "Alexou") {
        console.log("Kakou Kakou C'est Alexou");
    }else {
        console.log("C'est quoi???");
    }
}

switchColor(color);
